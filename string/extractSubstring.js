// Extract Substring (1): Methods are available to extract a portion of a string based on a starting position and the desired length.
// Before
// declaring a string variable
const text = "Javascript is Awesome!";
console.log("Before: " + text);

// After
// extracting a portion of the string with starting value 0(i.e., J) and length is 6(so until c)
console.log("After: " + text.substring(0, 6));
