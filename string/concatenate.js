// Concatenate: You can join two or more strings together using methods or operators.
// Before

// declaring variables and printing them out
const text1 = "Javascript";
const text2 = "is";
const text3 = "Awesome";
console.log("Before concatenating: ");
console.log(text1);
console.log(text2);
console.log(text3);

// After
// concatenating the variables (joining the text 1, text2, text3 strings together)
const result = text1.concat(" ", text2, " ", text3);
console.log("After concatenating: " + result);
