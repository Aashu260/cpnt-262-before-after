// Increment/Decrement: These operators provide a shorthand way to increase or decrease the value of a number by one.

// Before
// declaring the variable and printing it out
let number = 12;
console.log("Initial value: " + number);

// After
// incrementing the above value by 1
number++;
console.log("Increment value: " + number);

// incrementing the above value by 1
number--;
console.log("Decrement value: " + number);
